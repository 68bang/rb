	<h2>Sitemap이란?</h2>

	<p>
	Sitemap은 웹마스터가 크롤링에 사용할 수 있는 사이트의 페이지에 대한 정보를 검색 엔진에 알리는 손쉬운 방법입니다.<br />
	Sitemap의 가장 간단한 형식은 검색 엔진에서 사이트를 보다 지능적으로 크롤링할 수 있도록 각 URL에 대한 추가 메타데이터(마지막 업데이트된 날짜, 변경 빈도, 사이트의 다른 URL에 상대적인 중요도)와 함께 사이트에 대한 URL을 나열하는 XML 파일입니다.<br /><br />

	웹크롤러는 보통 해당 사이트 및 기타 사이트의 링크에서 페이지를 검색합니다.<br />
	Sitemap은 해당 데이터를 보완하여 Sitemap을 지원하는 크롤러가 해당 Sitemap에 있는 모든 URL을 선택하고 관련 메타데이터를 사용하여 이들 URL에 대해 파악할 수 있도록 합니다.<br />
	Sitemap 프로토콜을 사용하더라도 웹페이지가 반드시 검색 엔진에 포함되는 것은 아니지만 이를 통해 웹크롤러가 귀하의 사이트를 보다 효과적으로 크롤링하기 위한 힌트를 얻을 수 있습니다.<br /><br />

	Sitemap 0.90은 Attribution-ShareAlike Creative Commons License의 약관에 따라 제공되며 Google, Yahoo! 및 Microsoft의 지원을 비롯하여 널리 채택되고 있습니다.<br /><br />
	</p>


	<h2>구글 사이트맵 정보</h2>

	<p>
	Sitemap을 사용하면 Google이 발견하지 못했을 수도 있는 사이트의 페이지 정보를 Google에 알릴 수 있습니다.<br />
	간단히 말해서 XML Sitemap(일반적으로 Sitemap이라고 하며 S는 대문자로 표기)은 웹사이트에 있는 페이지의 목록입니다.<br />
	Sitemap을 만들어 제출하면 Google의 일반적인 크롤링 과정에서 발견되지 않는 URL을 비롯하여 사이트의 모든 페이지 정보를 Google에 알릴 수 있습니다.<br />
	</p>
	
	<div class="lightbulb">
	<a href="http://www.sitemaps.org/ko/" target="_blank">Sitemap 프로토콜</a>을 기반으로 하여 Sitemap을 만들거나 텍스트 파일 또는 RSS/Atom 피드를 Sitemap으로 제출할 수 있습니다. 
	<a href="https://support.google.com/webmasters/bin/answer.py?answer=183668" target="_blank">Sitemap을 만드는 방법</a> &nbsp; 
	<a href="https://www.google.co.kr/webmasters/" target="_blank">Google 웹마스터 도구</a>
	</div>

	<p>
	또한 Sitemap을 사용하여 동영상,이미지,모바일,뉴스 등과 같은 사용자 사이트 콘텐츠의 특정 유형에 대한 메타데이터를 Google에 제공할 수 있습니다.<br />
	예를 들어 동영상 Sitemap 항목은 동영상의 재생시간, 카테고리 및 등급을 지정할 수 있으며 이미지 Sitemap 항목은 이미지의 주제, 유형 및 라이센스에 대한 정보를 제공할 수 있습니다.<br />
	또한 Sitemap을 사용하여 마지막 업데이트한 날짜 및 페이지 변경 예상 빈도 등과 같은 <a href="https://support.google.com/webmasters/bin/answer.py?answer=71936" target="_blank">사이트에 대한 추가 정보</a>를 제공할 수도 있습니다.<br />
	별도의 Sitemap을 사용하여 뉴스 정보를 제출하는 것이 좋습니다.
	</p>
	
	<p>Sitemap이 특히 유용하게 사용되는 경우는 다음과 같습니다.</p>
	
	<ul>
	<li>동적 콘텐츠가 있는 사이트</li>
	<li>리치 AJAX나 이미지가 포함된 페이지와 같이 크롤링 과정 동안 Googlebot이 쉽게 검색하지 못하는 페이지가 있는 사이트</li>
	<li>연결되는 링크가 많지 않은 새로운 사이트(Googlebot은 페이지간 링크를 통해 웹을 크롤링합니다. 따라서 링크 연결이 잘 되어 있지 않은 사이트의 경우 Googlebot이 쉽게 검색하지 못할 수도 있습니다.)</li>
	<li>서로 잘 연결되지 않거나 전혀 연결되지 않는 콘텐츠 페이지를 보관하는 대규모 자료실이 있는 사이트</li>
	</ul>
	
	<p>Google은 모든 URL에 대해 크롤링하거나 색인을 생성할 수 있다고 보장하지는 않습니다.<br />
	하지만 Google은 Sitemap의 데이터를 사용하여 사이트의 구조를 파악하므로 크롤러 일정을 개선할 수 있으며 향후에 사이트를 더 효과적으로 크롤링할 수 있습니다.<br />
	대개 웹마스터는 Sitemap을 제출하는 것이 유리하며 이에 대해 어떤 경우에도 불이익을 받지 않습니다.
	</p>

	<p>Google은 <a href="http://www.sitemaps.org/ko/" target="_blank">sitemaps.org</a> 사이트에 명시된 바와 같이 Sitemap Protocol 0.9를 따릅니다.<br />
	따라서 Sitemap Protocol 0.9를 사용하여 만든 Google용 Sitemap은 sitemaps.org의 기준을 채택한 다른 검색엔진과 호환됩니다.<br />
	</p>

